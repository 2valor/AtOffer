package AtOffer

/**
 * 把一个整数减去1，再和原整数做与运算，会把该整数最右边一个1变成0
 */
func NumberOf1InBinary(n int) int {
	count := 0

	for 0 != n {
		n = n & (n-1)
		count++
	}

	return count
}
